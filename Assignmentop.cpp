//Assignment Operators

#include<iostream>
using namespace std;

int main()
{
	int x = 5;
	cout<< "Initial value of x is "<< x <<"\n";
	
	x +=5;
	cout<< "X += 5 gives : " << x <<"\n";
	
	x -=5;
	cout<< "X -= 5 gives : " << x <<"\n";
	
	x *=5;
	cout<< "X *= 5 gives : " << x <<"\n";
	
	x /=5;
	cout<< "X /= 5 gives : " << x <<"\n";
	
	return 0;
	
	
}
