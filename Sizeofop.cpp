//size of operator
//his operator determines a variable’s size. Use sizeof operator to determine the size of a data type

#include <iostream> 
using namespace std;
int main() 
{
	cout<<"Size of int : "<< sizeof(int) << "\n";

	cout<<"Size of char : " << sizeof(char) << "\n";

	cout<<"Size of float : " << sizeof(float) << "\n";

	cout<<"Size of double : " << sizeof(double) << "\n";

	return 0;
}
